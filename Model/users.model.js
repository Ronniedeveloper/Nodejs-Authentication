const mongoose = require('mongoose');

// Create schema
const userSchema = new mongoose.Schema({
    username: {
        type: String,
        unique: true,
        required: true
    },
    email: {
        type: String,
        unique: true,
        required: true
    },
    password: {
        type: String,
        required: true
    }
})

const user = mongoose.model('User', userSchema);
module.exports = user;